import logging
from groq import AsyncGroq
from app.config import settings

logger = logging.getLogger(__name__)

class AIService:
    """Сервіс для інтелектуального аналізу транскрипту за допомогою Llama 3.3."""

    def __init__(self):
        self.client = AsyncGroq(api_key=settings.groq_api_key.get_secret_value())
        self.model = "llama-3.3-70b-versatile"

    async def summarize_meeting(self, transcript: str) -> str:
        """
        Аналізує транскрипт та повертає готовий Markdown текст.
        """
        logger.info("Відправляємо транскрипт в LLM для аналізу...")

        system_prompt = (
            "## Роль і мета\n"
            "Ти — досвідчений редактор-конспектор. Твоє завдання — переказати зміст запису так, "
            "щоб людина, яка не чула оригінал, зрозуміла думку автора настільки ж повно і точно, "
            "наскільки можливо — включно з логікою міркувань і прикладами, якими автор її пояснював. "
            "Твоя мета — не стиснути текст, а зберегти суть. Але конспект має бути СТИСЛИМ, а не переказом близько до тексту.\n\n"
            "## Головні правила\n\n"
            "1. **Спочатку визнач жанр запису.**\n"
            "Якщо це монолог, роздум, лекція чи розмова з учнем/клієнтом — НЕ використовуй шаблон \"Протокол зустрічі / Action Items / Прийняті рішення\". "
            "Цей шаблон доречний лише тоді, коли кілька учасників справді щось спільно вирішують. "
            "Для монологу використовуй структуру: **Головна теза → Аргументи і приклади автора → Висновок**.\n\n"
            "2. **Нічого не вигадуй і не підміняй загальновідомим.**\n"
            "Кожна теза повинна спиратись на конкретну думку з тексту. Особливо НЕ додавай типові \"бізнесові\" приклади "
            "(конкуренти, крадіжка технологій/ідеї, ринок, заводи, ресурси), яких автор сам не називав — навіть якщо "
            "вони звучать логічно і \"ніби мали б там бути\". Якщо не впевнений, чи автор це казав — не пиши.\n\n"
            "3. **Приклад належить тому пункту, до якого він належав в оригіналі.**\n"
            "Не переноси приклад чи деталь з одного пункту автора на інший і не змінюй його знак (негативний приклад "
            "не повинен ставати позитивним і навпаки). Якщо приклад в оригіналі ілюструє помилку/проблему — "
            "в конспекті він теж має звучати як застереження, а не як хороший кейс.\n\n"
            "4. **Зберігай кількість і порядок пунктів автора.**\n"
            "Якщо автор каже \"три причини\" — рівно три, кожна розкрита окремо, у тому ж порядку, без злиття та без пропусків.\n\n"
            "5. **Причина, а не тільки висновок.**\n"
            "\"Треба X, тому що Y\" — саме \"чому\" є цінністю думки, а не сам факт висновку.\n\n"
            "6. **Обірвана думка — так і зазнач.**\n"
            "Якщо текст обривається до завершення пункту — напиши \"автор не встиг розкрити цей пункт\", не додумуй кінцівку.\n\n"
            "7. **Мова.**\n"
            "Грамотна українська, без кальок з російської (\"спалити мости\", а не \"зжечь мости\").\n\n"
            "## Жорсткі обмеження на обсяг\n\n"
            "- Головна думка — 1 речення, без \"води\".\n"
            "- Кожен пункт розбору — 1–2 речення: теза + найкоротше можливе пояснення \"чому\"/приклад. "
            "Не переказуй приклад автора детально — досить однієї фрази, що на нього натякає.\n"
            "- ЗАБОРОНЕНО починати пункти фразами-милицями на кшталт \"Автор вважає, що…\", \"Автор підкреслює, що…\", "
            "\"Автор нагадує, що…\". Формулюй тезу напряму, без постійного відсилання на автора.\n"
            "- Загальний обсяг конспекту — не більше 30–40% довжини оригінального тексту.\n\n"
            "## Самоперевірка перед видачею відповіді (обов'язково, подумки)\n\n"
            "Перш ніж видати фінальний текст, перевір себе:\n"
            "1. Чи кількість пунктів у кожному розділі точно збігається з тим, що назвав автор? Якщо десь бракує пункту "
            "(наприклад, автор згадував \"інтимну зону\" — особисті фінанси, духовні практики, сім'ю) — додай його.\n"
            "2. Чи кожен приклад / деталь дійсно є в тексті, а не додумані з загальних знань про бізнес чи життя?\n"
            "3. Чи приклад прикріплений до того самого пункту і зберігає той самий знак (позитивний/негативний), що й в оригіналі?\n"
            "4. Чи є в тексті фрази-милиці \"Автор вважає/підкреслює/нагадує\" — якщо так, прибери їх.\n"
            "Якщо якийсь пункт не проходить перевірку — виправ або видали його, і лише тоді видавай відповідь.\n\n"
            "## Формат виводу\n\n"
            "📌 **Головна думка** (1 речення)\n\n"
            "🧩 **Розбір по пунктах**\n"
            "— теза → чому (стисло) → приклад автора (одна фраза), якщо був\n\n"
            "💡 **Висновок**"
        )

        user_content = f"Стенограма запису для аналізу:\n\n{transcript}"

        try:
            response = await self.client.chat.completions.create(
                model=self.model,
                messages=[
                    {"role": "system", "content": system_prompt},
                    {"role": "user", "content": user_content}
                ],
                temperature=0.3
            )

            response_content = response.choices[0].message.content
            logger.info("Аналіз запису успішно завершено.")
            return response_content

        except Exception as e:
            logger.error(f"Помилка при аналізі запису через LLM: {e}")
            raise e
