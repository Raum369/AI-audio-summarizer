import logging
from groq import AsyncGroq
from app.config import settings

logger = logging.getLogger(__name__)

class AIService:
    """Сервіс для інтелектуального аналізу транскрипту за допомогою Llama 3.3."""

    def __init__(self):
        self.client = AsyncGroq(api_key=settings.groq_api_key.get_secret_value())
        self.model = "llama-3.3-70b-versatile"

    async def summarize_meeting(self, transcript: str) -> str:
        """
        Аналізує транскрипт та повертає готовий Markdown текст.
        Використовує Chain of Thought (<scratchpad>) для уникнення галюцинацій та збереження точності.
        """
        logger.info("Відправляємо транскрипт в LLM для аналізу...")

        system_prompt = (
            "Ти — високоточний аналітик і редактор-конспектор. Твоє завдання — переказати аудіозапис максимально точно, суворо дотримуючись змісту без жодних вигадок.\n\n"
            "## АЛГОРИТМ РОБОТИ (ЛАНЦЮЖОК МІРКУВАНЬ)\n"
            "Перед тим як писати фінальний конспект, ти ОБОВ'ЯЗКОВО повинен проаналізувати текст у внутрішньому блоці <scratchpad>...</scratchpad>.\n"
            "У цьому блоці виконай наступне:\n"
            "1. Визнач усі пункти по порядку. Зверни особливу увагу на НАЙПЕРШИЙ пункт (інтимна зона: особисті фінанси, духовні практики чи сім'я — це повноцінний пункт, який категорично заборонено пропускати).\n"
            "2. Для кожного пункту знайди ПРЯМИЙ приклад з тексту. Приклад про політиків є НЕГАТИВНИМ (політики самі вирішують за інших і це погано закінчується), його заборонено перекручувати на позитивний або переставляти до іншого пункту.\n"
            "3. Перевір текст на наявність вигадок. Суворо забороняється додавати інформацію про 'технології, які конкуренти можуть забрати і реалізувати швидше' або інший бізнес-жаргон, якщо цього немає в оригіналі.\n"
            "4. Перевірка на русизми: якщо в тексті зустрічається калька 'зжечі мости', обов'язково виправ її на 'спалити мости'.\n\n"
            "## СУВОРІ ПРАВИЛА КОНСПЕКТУВАННЯ\n"
            "1. **ЖОДНИХ ГАЛЮЦИНАЦІЙ.** Використовуй ТІЛЬКИ ті факти і деталі, які прямо названі в тексті.\n"
            "2. **Повнота пунктів.** Перший пункт про 'інтимну зону' має бути збережений обов'язково.\n"
            "3. **Без води.** Замість 'Автор вважає, що...' пиши відразу суть: Теза → Чому → Приклад.\n\n"
            "## ПРИКЛАД АНАЛІЗУ (FEW-SHOT)\n"
            "<scratchpad>\n"
            "1. Жанр тексту: монолог\n"
            "2. Список пунктів:\n"
            "   - Пункт 1: Не ділитися планами в особистих сферах (інтимна зона: сім'я, фінанси, духовні практики) | Приклад: немає | Тональність: застереження\n"
            "   - Пункт 2: Чому не можна розказувати про цілі | Приклад: політики роблять публічні заяви і самі вирішують за людей, а потім не виконують обіцянки, що веде до розчарування | Тональність: негативна (як робити не треба)\n"
            "3. Самоперевірка:\n"
            "   - Пункт про інтимну зону (фінанси/сім'я/духовність) збережено? ТАК.\n"
            "   - Галюцинації про 'технології/конкурентів' є? НІ, викреслено.\n"
            "   - Приклад з політиками подано як негативний? ТАК.\n"
            "   - 'Зжечі мости' замінено на 'спалити мости'? ТАК.\n"
            "</scratchpad>\n\n"
            "## ФОРМАТ ВИВОДУ\n"
            "<scratchpad>\n"
            "[Твій аналіз за прикладом вище]\n"
            "</scratchpad>\n\n"
            "📌 **Головна думка** (1 речення, стисло)\n\n"
            "🧩 **Розбір по пунктах**\n"
            "— [Теза] → [Чому (найкоротше пояснення)] → [Приклад автора (збережено тональність і суть)]\n\n"
            "💡 **Висновок**"
        )

        user_content = f"Стенограма запису для аналізу:\n\n{transcript}"

        try:
            response = await self.client.chat.completions.create(
                model=self.model,
                messages=[
                    {"role": "system", "content": system_prompt},
                    {"role": "user", "content": user_content}
                ],
                temperature=0.1  # Знижено температуру для більшої точності та зменшення галюцинацій
            )

            response_content = response.choices[0].message.content
            
            # Видаляємо блок <scratchpad> з фінального результату, щоб користувач бачив лише чистий Markdown
            import re
            clean_content = re.sub(r'<scratchpad>.*?</scratchpad>', '', response_content, flags=re.DOTALL).strip()
            
            logger.info("Аналіз запису успішно завершено (Prompt V3 CoT).")
            return clean_content

        except Exception as e:
            logger.error(f"Помилка при аналізі запису через LLM: {e}")
            raise e
