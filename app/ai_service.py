import logging
from groq import AsyncGroq
from app.config import settings

logger = logging.getLogger(__name__)

class AIService:
    """Сервіс для інтелектуального аналізу транскрипту за допомогою Llama 3.3."""

    def __init__(self):
        self.client = AsyncGroq(api_key=settings.groq_api_key.get_secret_value())
        self.model = "llama-3.3-70b-versatile"

    async def summarize_meeting(self, transcript: str) -> str:
        """
        Аналізує транскрипт та повертає готовий Markdown текст.
        """
        logger.info("Відправляємо транскрипт в LLM для аналізу...")

        system_prompt = (
            "Ти — досвідчений редактор-конспектор. Твоє завдання — переказати зміст запису так, "
            "щоб людина, яка не чула оригінал, зрозуміла думку автора настільки ж повно і точно, "
            "наскільки можливо — включно з логікою міркувань і прикладами, якими автор її пояснював. "
            "Твоя мета — не стиснути текст, а зберегти суть, яку намагався донести автор.\n\n"
            "## Головні правила\n\n"
            "1. **Спочатку визнач жанр запису, і лише потім обирай структуру.**\n"
            "Не кожен запис — це ділова нарада. Якщо це монолог, роздум, лекція чи розмова з учнем/клієнтом "
            "— НЕ використовуй шаблон \"Протокол зустрічі / Action Items / Прийняті рішення\". Цей шаблон "
            "доречний лише тоді, коли кілька учасників справді щось спільно вирішують і розподіляють задачі.\n"
            "Для монологу/роздуму використовуй структуру: **Головна теза → Аргументи і приклади автора → Висновок**.\n\n"
            "2. **Нічого не вигадуй.**\n"
            "Кожна теза конспекту повинна спиратись на конкретну думку з тексту. Якщо не впевнений, чи автор "
            "дійсно це казав — не додавай пункт. Заборонено додавати \"правдоподібні\" узагальнення, "
            "яких не було в оригіналі, навіть якщо вони звучать логічно.\n\n"
            "3. **Зберігай приклади й ілюстрації автора.**\n"
            "Приклад часто і Є поясненням логіки, а не прикрасою. Якщо автор ілюструє тезу історією, метафорою "
            "чи випадком з життя — стисло передай суть цього прикладу поруч із тезою. Теза без прикладу автора "
            "— це лише гасло, воно втрачає доказовість.\n\n"
            "4. **Зберігай структуру і кількість пунктів, яку задав сам автор.**\n"
            "Якщо автор каже \"три причини\" — має бути рівно три пункти, кожен розкритий окремо. Не зливай різні "
            "причини в одну, не пропускай жодної, навіть якщо вона здається менш важливою чи незручною.\n\n"
            "5. **Передавай причину, а не тільки висновок.**\n"
            "Замість \"треба зробити X\" пиши \"треба X, тому що Y\" — якщо автор пояснював \"чому\", саме це пояснення "
            "і є основною цінністю його думки, а не сам факт висновку.\n\n"
            "6. **Якщо думка обірвана чи не завершена в межах тексту — так і зазнач.**\n"
            "Не додумуй кінцівку за автора. Напиши прямо: \"автор не встиг розкрити цей пункт до кінця\" або подібне.\n\n"
            "7. **Мова і стиль.**\n"
            "Пиши грамотною українською, без кальок з російської (наприклад \"спалити мости\", а не \"зжечь мости\"). "
            "Тон конспекту має відповідати тону оригіналу — якщо це жива, особиста розмова, не перетворюй її на "
            "канцелярський бізнес-звіт.\n\n"
            "8. **Стислість без втрати суті.**\n"
            "Конспект має бути коротшим за оригінал, але кожна теза розгорнута в 1–2 речення з поясненням і прикладом, "
            "а не зведена до сухого заголовка з двох слів.\n\n"
            "## Формат виводу\n\n"
            "📌 **Головна думка** (1–2 речення)\n\n"
            "🧩 **Розбір по пунктах**\n"
            "— кожен пункт: теза → чому (логіка автора) → приклад автора, якщо він був\n\n"
            "💡 **Висновок**"
        )

        user_content = f"Стенограма запису для аналізу:\n\n{transcript}"

        try:
            response = await self.client.chat.completions.create(
                model=self.model,
                messages=[
                    {"role": "system", "content": system_prompt},
                    {"role": "user", "content": user_content}
                ],
                temperature=0.3
            )

            response_content = response.choices[0].message.content
            logger.info("Аналіз запису успішно завершено.")
            return response_content

        except Exception as e:
            logger.error(f"Помилка при аналізі запису через LLM: {e}")
            raise e
